public class SwitchPro {
    public static void main(String[] args) {
        char ch =65;
        switch (ch){
            case 1 :
                System.out.println("1");
                break;
            case 'B' :
                System.out.println("2");
                break;

            case 'd' :
                System.out.println("Char A");
                break;
            default:
                System.out.println("No match");
        }
    }
}
